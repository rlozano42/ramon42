#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*aux;
	size_t	cont;

	aux = (char *)s;
	cont = 0;
	while (cont < n)
	{
		*aux = '\0';
		cont++;
		aux++;
	}
}
