int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int l;

	l = 0;
	if (n == 0)
		return (0);
	while ((*s1 || *s2) && l < n)
	{
		if (*s1 == *s2)
		{
			s1++;
			s2++;
			l++;
		}
		else if (*s1 != *s2)
			return (*s1 - *s2);
	}
	return (0);
}
