int		ft_str_is_numeric(char *str)
{
	int n;
	int i;

	i = 1;
	n = 0;
	if (str[0] == '\0')
		i = 1;
	while (*str)
	{
		if ((*str >= 48 && *str <= 90))
		{
			i = 1;
		}
		else
		{
			i = 0;
			return (i);
		}
		str++;
	}
	return (i);
}
