#include <unistd.h>

int		ft_str_is_lowercase(char *str)
{
	int n;
	int i;

	i = 1;
	n = 0;
	if (str[0] == '\0')
		i = 1;
	while (*str)
	{
		if ((*str >= 97 && *str <= 122))
		{
			i = 1;
		}
		else
		{
			i = 0;
			return (i);
		}
		str++;
	}
	return (i);
}
