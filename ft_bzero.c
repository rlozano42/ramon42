#include "libft.h"

void    ft_bzero(void *s,size_t n)
{
    char    *aux;
    size_t    cont;

    cont = 0;
    aux = (char*)s;
    while (cont < n)
    {
        aux[cont] = 0;
        cont++;
    }
}
