#include <unistd.h>

void	ft_print_comb2()
{
	char a;
	char b;
	char c;
	char d;

	a = '0';
	b = '0';
	c = '0';
	d = '1';
	while (c <= '9' && d <= '9')
	{
		write(1, &a, 1);
		write(1, &b, 1);
		write(1, " ", 1);
		write(1, &c, 1);
		write(1, &d, 1); 
		if (!(a ==  '9' && b == '8' && c == '9' && d == '9'))
			write(1, ", ", 2);
		if (c != '9' && d == '9')
		{
			c++;
			d = '1';
		}
		else if (c == '9' && d == '9')
		{
			b++;
			if(b == ':')
			{
				a++;
				b='0';
			}
			c = a;
			d = b + 1;;
			if (d == ':')
			{
				c++;
				d = '0';
			}
		}
		else
		{
			d++;
		}
	}
}
